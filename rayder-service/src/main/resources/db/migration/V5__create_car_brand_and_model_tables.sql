CREATE TABLE IF NOT EXISTS car_brands (
    id BIGINT PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    country VARCHAR(100) NOT NULL,
    category VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS car_models (
    id BIGINT PRIMARY KEY,
    brand_id BIGINT NOT NULL,
    name VARCHAR(100) NOT NULL,
    CONSTRAINT fk_car_models_brand FOREIGN KEY (brand_id) REFERENCES car_brands (id) ON DELETE CASCADE,
    CONSTRAINT uq_car_models_brand_name UNIQUE (brand_id, name)
);

CREATE INDEX idx_car_models_brand_id ON car_models (brand_id);

